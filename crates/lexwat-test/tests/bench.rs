fn dump_corpus() {
    // Used by the wasm2c+clang experiment so it lexes byte-identical input.
    let src = corpus(512 * 1024);
    std::fs::write("/tmp/corpus.bin", &src).unwrap();
    println!("wrote {} bytes to /tmp/corpus.bin", src.len());
}

#[test]
#[ignore]
fn benchmark() {
    let src = corpus(512 * 1024);
    let bytes = src.len();
    let tokens = rust_token_count(&src);
    let iters = 100;
    println!(
        "\ncorpus: {bytes} bytes, {tokens} tokens/pass, {iters} passes\n"
    );

    time("rust (logos, spanned)", iters, bytes, || rust_token_count(&src));
    time("rust (logos, no span)", iters, bytes, || rust_token_count_nospan(&src));
    time("rust hand-rolled (native)", iters, bytes, || {
        lexwat_test::hand_count(src.as_bytes())
    });
    // ceiling: a tight native byte scan (no lexing) — memory-bandwidth bound
    time("rust byte-sum (native)", iters, bytes, || {
        src.as_bytes().iter().fold(0usize, |a, &b| a + b as usize)
    });

    let engine = Engine::default();
    let mut r_if = wasm_runner(&engine, "lex.wat");
    let mut r_bt = wasm_runner(&engine, "lex_brtable.wat");
    let mut r_nm = wasm_runner(&engine, "lex_nomem.wat");
    let mut r_nb = wasm_runner(&engine, "lex_nomem_brtable.wat");
    time("wasm lex.wat (if-chain)", iters, bytes, || r_if(&src));
    time("wasm lex_brtable.wat", iters, bytes, || r_bt(&src));
    time("wasm lex_nomem.wat", iters, bytes, || r_nm(&src));
    time("wasm lex_nomem_brtable.wat", iters, bytes, || r_nb(&src));

    println!("  -- source in memory, no per-byte boundary crossing --");
    let self_wasm = wat::parse_file(crate_file("lex_selfsource.wat")).unwrap();
    let mut r_self = wasm_mem_runner(&engine, &self_wasm, 1024);
    time("wasm lex_selfsource.wat", iters, bytes, || r_self(&src));
    let self_opt = std::fs::read(out_file("self_opt.wasm")).unwrap();
    let mut r_self_opt = wasm_mem_runner(&engine, &self_opt, 1024);
    time("  + wasm-opt -O3 (inlined)", iters, bytes, || r_self_opt(&src));

    // The wasm-merge output of driver.wat + lex_nomem.wat (generated by build.rs).
    let merged = std::fs::read(out_file("merged.wasm")).unwrap();
    let mut r_merged = wasm_mem_runner(&engine, &merged, 0);
    time("wasm merged (driver+nomem)", iters, bytes, || r_merged(&src));

    println!("  -- whole lex in one call: also internalize the next_token loop --");
    let merged2 = std::fs::read(out_file("merged2.wasm")).unwrap();
    let mut r_merged2 = run_once_runner(&merged2);
    time("wasm merged2 run() one call", iters, bytes, || r_merged2(&src));
    let merged2_opt = std::fs::read(out_file("merged2_opt.wasm")).unwrap();
    let mut r_merged2_opt = run_once_runner(&merged2_opt);
    time("  + wasm-opt -O3 (inlined)", iters, bytes, || r_merged2_opt(&src));

    // Isolate the per-byte host-call overhead: a module that only drains next_byte.
    let probe_wat = r#"(module
        (import "env" "next_byte" (func $nb (result i32)))
        (func (export "reset"))
        (func (export "next_token") (result i32)
          (local $n i32)
          (loop $l (if (i32.ge_s (call $nb) (i32.const 0))
            (then (local.set $n (i32.add (local.get $n) (i32.const 1))) (br $l))))
          (i32.const 0)))"#;
    let wasm = wat::parse_str(probe_wat).unwrap();
    let module = Module::new(&engine, &wasm).unwrap();
    let mut store = Store::new(&engine, Host { src: Vec::new(), idx: 0 });
    let mut linker = Linker::new(&engine);
    linker
        .func_wrap("env", "next_byte", |mut caller: Caller<'_, Host>| -> i32 {
            let h = caller.data_mut();
            match h.src.get(h.idx) {
                Some(&b) => { h.idx += 1; b as i32 }
                None => -1,
            }
        })
        .unwrap();
    let instance = linker.instantiate(&mut store, &module).unwrap();
    let nt = instance.get_typed_func::<(), i32>(&mut store, "next_token").unwrap();
    store.data_mut().src.extend_from_slice(src.as_bytes());
    time("wasm next_byte drain only", iters, bytes, || {
        store.data_mut().idx = 0;
        nt.call(&mut store, ()).unwrap();
        tokens
    });

    // ceiling: a tight in-wasm byte scan over memory (no calls, one bulk write).
    let scan_wat = r#"(module
        (memory (export "memory") 256)
        (global $len (mut i32) (i32.const 0))
        (func (export "set_len") (param $n i32) (global.set $len (local.get $n)))
        (func (export "next_token") (result i32)   ;; sums bytes
          (local $i i32) (local $s i32)
          (loop $l (if (i32.lt_u (local.get $i) (global.get $len))
            (then
              (local.set $s (i32.add (local.get $s) (i32.load8_u (local.get $i))))
              (local.set $i (i32.add (local.get $i) (i32.const 1)))
              (br $l))))
          (local.get $s)))"#;
    let wasm = wat::parse_str(scan_wat).unwrap();
    let module = Module::new(&engine, &wasm).unwrap();
    let mut store = Store::new(&engine, Host { src: Vec::new(), idx: 0 });
    let instance = Linker::new(&engine).instantiate(&mut store, &module).unwrap();
    let mem = instance.get_memory(&mut store, "memory").unwrap();
    let set_len = instance.get_typed_func::<i32, ()>(&mut store, "set_len").unwrap();
    let scan = instance.get_typed_func::<(), i32>(&mut store, "next_token").unwrap();
    mem.write(&mut store, 0, src.as_bytes()).unwrap();
    set_len.call(&mut store, src.len() as i32).unwrap();
    time("wasm byte-scan ceiling", iters, bytes, || {
        scan.call(&mut store, ()).unwrap() as usize
    });
    println!();
}
