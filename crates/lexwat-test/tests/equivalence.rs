//! Differential test: every `lex*.wat` variant's `next_token` stream must match
//! `moss-core`'s logos lexer, across curated cases, all repo `.moss` files, and
//! fuzzed inputs.

use std::path::{Path, PathBuf};

use logos::Logos;
use moss_core::lex::Token;
use wasmtime::*;

/// Host state: the source bytes and a forward-only read cursor for `next_byte`.
struct Host {
    src: Vec<u8>,
    idx: usize,
}

/// The token-id sequence the real lexer produces, using the protocol the WASM
/// modules promise: each token's enum discriminant in order, then `0` (Eof) on
/// success, or `-1` at the first invalid token (mirroring `lex` returning Err).
fn expected(src: &str) -> Vec<i64> {
    let mut out = Vec::new();
    for (result, _range) in Token::lexer(src).spanned() {
        match result {
            Ok(token) => out.push(token as i64),
            Err(_) => {
                out.push(-1);
                return out;
            }
        }
    }
    out.push(Token::Eof as i64); // == 0
    out
}

fn repo_root() -> PathBuf {
    Path::new(env!("CARGO_MANIFEST_DIR"))
        .join("../..")
        .canonicalize()
        .unwrap()
}

/// A `.wat` source that lives alongside this crate.
fn crate_file(name: &str) -> PathBuf {
    Path::new(env!("CARGO_MANIFEST_DIR")).join(name)
}

/// A `.wasm` artifact generated by build.rs into OUT_DIR (via binaryen).
fn out_file(name: &str) -> PathBuf {
    Path::new(env!("OUT_DIR")).join(name)
}

fn module(engine: &Engine, wat_name: &str) -> Module {
    let wasm = wat::parse_file(crate_file(wat_name)).expect("wat should assemble");
    Module::new(engine, &wasm).expect("module should validate")
}

/// Drive the WASM lexer over `src`, returning its emitted token-id sequence.
fn actual(engine: &Engine, module: &Module, src: &str) -> Vec<i64> {
    let mut store = Store::new(
        engine,
        Host {
            src: src.as_bytes().to_vec(),
            idx: 0,
        },
    );
    let mut linker = Linker::new(engine);
    linker
        .func_wrap("env", "next_byte", |mut caller: Caller<'_, Host>| -> i32 {
            let host = caller.data_mut();
            match host.src.get(host.idx) {
                Some(&b) => {
                    host.idx += 1;
                    b as i32
                }
                None => -1,
            }
        })
        .unwrap();
    let instance = linker.instantiate(&mut store, module).unwrap();
    let next_token = instance
        .get_typed_func::<(), i32>(&mut store, "next_token")
        .unwrap();

    let mut out = Vec::new();
    for _ in 0..(src.len() * 2 + 16) {
        let id = next_token.call(&mut store, ()).unwrap() as i64;
        out.push(id);
        if id <= 0 {
            return out; // Eof (0) or invalid (-1)
        }
    }
    panic!("next_token did not terminate for input {src:?}: {out:?}");
}

fn check(engine: &Engine, module: &Module, src: &str) {
    assert_eq!(
        expected(src),
        actual(engine, module, src),
        "mismatch for input {src:?}"
    );
}

/// Recursively collect every `.moss` file under the repo root.
fn moss_files() -> Vec<(PathBuf, String)> {
    fn walk(dir: &Path, out: &mut Vec<(PathBuf, String)>) {
        let Ok(entries) = std::fs::read_dir(dir) else {
            return;
        };
        for entry in entries.flatten() {
            let path = entry.path();
            if path.is_dir() {
                if path.file_name().is_some_and(|n| n == "target" || n == ".git") {
                    continue;
                }
                walk(&path, out);
            } else if path.extension().is_some_and(|e| e == "moss") {
                if let Ok(text) = std::fs::read_to_string(&path) {
                    out.push((path, text));
                }
            }
        }
    }
    let mut out = Vec::new();
    walk(&repo_root(), &mut out);
    out.sort();
    assert!(!out.is_empty(), "expected to find .moss files");
    out
}

