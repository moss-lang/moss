fn run_suite(wat_name: &str) {
    let engine = Engine::default();
    let module = module(&engine, wat_name);

    for src in CURATED {
        check(&engine, &module, src);
    }

    for (path, text) in moss_files() {
        assert_eq!(
            expected(&text),
            actual(&engine, &module, &text),
            "mismatch for moss file {} ({wat_name})",
            path.display()
        );
    }

    // ---- random ASCII fuzz ----
    let charset: &[u8] = b"abxyz_AZ0123459 \t\n\r#!%&()*+,-./:;<=>[]^{|}'\"\\uiUI32649@?~";
    let mut state: u64 = 0x9e37_79b9_7f4a_7c15;
    let mut next = || {
        state = state
            .wrapping_mul(6364136223846793005)
            .wrapping_add(1442695040888963407);
        (state >> 33) as usize
    };
    for _ in 0..12_000 {
        let len = next() % 24;
        let bytes: Vec<u8> = (0..len).map(|_| charset[next() % charset.len()]).collect();
        check(&engine, &module, std::str::from_utf8(&bytes).unwrap());
    }

    // ---- random UTF-8 literal fuzz (char/string contents, interleaved tokens) ----
    let char_scalars = ['a', '0', ' ', '\'', '\\', 'é', '😀', 'λ', '\u{00a0}', 'π', '\n'];
    let str_scalars = ['a', ' ', 'é', '😀', 'λ', '\u{00a0}', 'π', 'b', '9', '\r'];
    let ascii_atoms = ["let", "x", "::", "5u32", "->", "-7i64", "+", "(", ")", "."];
    let mut state: u64 = 0x1234_5678_9abc_def0;
    let mut next = || {
        state = state
            .wrapping_mul(6364136223846793005)
            .wrapping_add(1442695040888963407);
        (state >> 33) as usize
    };
    for _ in 0..7_000 {
        let atoms = 1 + next() % 6;
        let mut src = String::new();
        for _ in 0..atoms {
            match next() % 3 {
                0 => {
                    src.push('\'');
                    src.push(char_scalars[next() % char_scalars.len()]);
                    src.push('\'');
                }
                1 => {
                    src.push('"');
                    for _ in 0..(next() % 4) {
                        src.push(str_scalars[next() % str_scalars.len()]);
                    }
                    src.push('"');
                }
                _ => src.push_str(ascii_atoms[next() % ascii_atoms.len()]),
            }
            src.push(' ');
        }
        check(&engine, &module, &src);
    }
}

