/// Drive a module that reads its source from memory (host writes it at `offset`,
/// then calls set_len + reset + next_token).
fn actual_mem(engine: &Engine, module: &Module, offset: usize, src: &str) -> Vec<i64> {
    let mut store = Store::new(engine, ());
    let instance = Linker::new(engine)
        .instantiate(&mut store, module)
        .unwrap();
    let memory = instance.get_memory(&mut store, "memory").unwrap();
    memory.write(&mut store, offset, src.as_bytes()).unwrap();
    if let Ok(reset) = instance.get_typed_func::<(), ()>(&mut store, "reset") {
        reset.call(&mut store, ()).unwrap();
    }
    instance
        .get_typed_func::<i32, ()>(&mut store, "set_len")
        .unwrap()
        .call(&mut store, src.len() as i32)
        .unwrap();
    let next_token = instance
        .get_typed_func::<(), i32>(&mut store, "next_token")
        .unwrap();
    let mut out = Vec::new();
    for _ in 0..(src.len() * 2 + 16) {
        let id = next_token.call(&mut store, ()).unwrap() as i64;
        out.push(id);
        if id <= 0 {
            return out;
        }
    }
    panic!("next_token did not terminate for {src:?}: {out:?}");
}

fn run_suite_mem(wasm: &[u8], offset: usize) {
    let engine = Engine::default();
    let module = Module::new(&engine, wasm).unwrap();
    for src in CURATED {
        assert_eq!(expected(src), actual_mem(&engine, &module, offset, src), "{src:?}");
    }
    for (path, text) in moss_files() {
        assert_eq!(
            expected(&text),
            actual_mem(&engine, &module, offset, &text),
            "moss file {}",
            path.display()
        );
    }
}

