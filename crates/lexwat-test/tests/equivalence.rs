/// Drive merged2 (driver2 + lexer): write bytes, run() the whole lex in one
/// call, then read the token ids back out of the second memory.
fn actual_run(engine: &Engine, module: &Module, src: &str) -> Vec<i64> {
    let mut store = Store::new(engine, ());
    let instance = Linker::new(engine).instantiate(&mut store, module).unwrap();
    let bytes = instance.get_memory(&mut store, "bytes").unwrap();
    let toks = instance.get_memory(&mut store, "toks").unwrap();
    bytes.write(&mut store, 0, src.as_bytes()).unwrap();
    instance
        .get_typed_func::<(), ()>(&mut store, "reset")
        .unwrap()
        .call(&mut store, ())
        .unwrap();
    instance
        .get_typed_func::<i32, ()>(&mut store, "set_len")
        .unwrap()
        .call(&mut store, src.len() as i32)
        .unwrap();
    let count = instance
        .get_typed_func::<(), i32>(&mut store, "run")
        .unwrap()
        .call(&mut store, ())
        .unwrap() as usize;
    let mut buf = vec![0u8; count * 4];
    toks.read(&store, 0, &mut buf).unwrap();
    buf.chunks_exact(4)
        .map(|c| i32::from_le_bytes(c.try_into().unwrap()) as i64)
        .collect()
}

#[test]
fn wasm_opt_artifacts_still_correct() {
    // wasm-opt -O3 must not change behavior.
    run_suite_mem(&std::fs::read(out_file("self_opt.wasm")).unwrap(), 1024);

    let mut cfg = Config::new();
    cfg.wasm_multi_memory(true);
    let engine = Engine::new(&cfg).unwrap();
    let module = Module::new(&engine, &std::fs::read(out_file("merged2_opt.wasm")).unwrap()).unwrap();
    for src in CURATED {
        assert_eq!(expected(src), actual_run(&engine, &module, src), "{src:?}");
    }
    for (path, text) in moss_files() {
        assert_eq!(expected(&text), actual_run(&engine, &module, &text), "{}", path.display());
    }
}

